/*************************************************
 *                   DRAW LINE                   *
 * **********************************************/

/*
	NOTE: This code was based on pseudo code from a tutorial at 
	freecodecamp.org. It is based on Bresenham’s algorithm
	for drawing lines.

	AUTHOR: goes by Javascript Teacher, actual name is unknown

	REFERENCE: https://www.freecodecamp.org/news/how-to-code-your-first-algorithm-draw-a-line-ca121f9a1395/
*/

void draw_line (PARAM_VGA_DRAW_LINE * params) 
{

	if (params->window_id == NO_WINDOW)
		return;

	window * wnd = &windows[params->window_id];

	int x, y, dx, dy, dx1, dy1, px, py, xe, ye, i;

	dx = params->x1 - params->x0;
	dy = params->y1 - params->y0;

	dx1 = m_abs(dx);
	dy1 = m_abs(dy);

	px = 2 * dy1 - dx1;
	py = 2 * dx1 - dy1;

	if (dy1 <= dx1) {

		if (dx >= 0) {
			x = params->x0;
			y = params->y0;
			xe = params->x1;
		} else {
			x = params->x1;
			y = params->y1;
			xe = params->x0;
		}

		set_pixel(wnd, x, y, params->color, CANVAS);

		for (i = 0; x < xe; i++) {
			x += 1;

			if (px < 0) {
				px = px + 2 * dy1;
			} else {
				if ((dx < 0 && dy < 0) || (dx > 0 && dy > 0)) {
					y += 1;
				} else {
					y -= 1;
				}
				px = px + 2 * (dy1 - dx1);
			}

			set_pixel(wnd, x, y, params->color, CANVAS);
		}

	} else {

		if (dy >= 0) {
			x = params->x0;
			y = params->y0;
			ye = params->y1;
		} else {
			x = params->x1;
			y = params->y1;
			ye = params->y0;
		}

		set_pixel(wnd, x, y, params->color, CANVAS);

		for (i = 0; y < ye; i++) {
			y += 1;

			if (py < 0) {
				py = py + 2 * dx1;
			} else {
				if ((dx < 0 && dy < 0) || (dx > 0 && dy > 0)) {
					x += 1;
				} else {
					x -= 1;
				}
				py = py + 2 * (dx1 - dy1);
			}

            		set_pixel(wnd, x, y, params->color, CANVAS);
			
		}

	}
}

